public class GeneralClassifierException extends Exception {

    public GeneralClassifierException() {
        super();
    }

    public GeneralClassifierException(String message) {
        super(message);
    }

    @Override
    public String getMessage() {
        return super.getMessage();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
